package Lessons;

public class Caller {

    private String name;
    private int id;
    private static int nextId = 0;

    public Caller(String n) {
        name = n;
        nextId++;
        id = nextId;

    }

    @Override
    public String toString() {
        return ("Caller: " + name + "\tID: " + id); 
    }

}
