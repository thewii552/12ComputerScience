package Project.Conference;

import java.util.Objects;

public class Guest {

    String name, email;

    public Guest(String n, String e) {
        name = n;
        email = e;

    }
    
    public Guest(String info){
        //Convert information from data file into a guest
        int split = info.indexOf("<");
        name = info.substring(0,split);
        email = info.substring(split=1, info.length()-1);
    }
    
    @Override
    public String toString(){
        return name + "<"+email+">";
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 41 * hash + Objects.hashCode(this.name);
        hash = 41 * hash + Objects.hashCode(this.email);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Guest other = (Guest) obj;
        if (!Objects.equals(this.name, other.name)) {
            return false;
        }
        if (!Objects.equals(this.email, other.email)) {
            return false;
        }
        return true;
    }
    
    
}
