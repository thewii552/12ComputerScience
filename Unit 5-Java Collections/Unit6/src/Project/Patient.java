package Project;

public class Patient {

    private String name, condition;

    public Patient(String n, String c) {
        name = n;
        condition = c;
    }
    
    @Override
    public String toString(){
        return name +"\t\t"+ condition;
    }
    
    public String getName(){
        return name;
    }

}
