package Projects;

import TerminalIO.*;

public class FibonacciCalc {

    public static void main(String args[]) {
        System.out.println(fibonacci(5));
    }

    public static long fibonacci(int n) {
        if (n <= 2) {
            return 1;
        }
        else {
            return fibonacci(n-1)+fibonacci(n-2);
        }
    }

}
