package JIRC.core;

import java.util.Objects;



public class User {

    String name;
    
    
    public User(String n) {
        name = n;
    }
    
    @Override
    public String toString(){//Return the user as a string
        return name; //Name for now, but could change
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 97 * hash + Objects.hashCode(this.name);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final User other = (User) obj;
        if (!Objects.equals(this.name, other.name)) {
            return false;
        }
        return true;
    }
    
    
}
